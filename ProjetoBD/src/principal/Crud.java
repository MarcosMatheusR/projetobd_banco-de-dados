    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        int fila = tabela.getSelectedRow();
        
        Formulario_Update f = new Formulario_Update(this, true);
        f.txtNome.setText(tabela.getValueAt(fila, 1).toString());
        f.txtUsuario.setText(tabela.getValueAt(fila, 2).toString());
        f.txtPassword.setText(tabela.getValueAt(fila, 3).toString());
        if(tabela.getValueAt(fila, 4).toString() == "M"){
            f.rbtMasculino.setSelected(true);
        } else {
            f.rbtFeminino.setSelected(true);
        }
        f.txtEndereco.setText(tabela.getValueAt(fila, 5).toString());
        f.txtBairro.setText(tabela.getValueAt(fila, 6).toString());
        f.txtCidade.setText(tabela.getValueAt(fila, 7).toString());
        f.txtCep.setText(tabela.getValueAt(fila, 8).toString());
        f.txtEmail.setText(tabela.getValueAt(fila, 9).toString());
        if(tabela.getValueAt(fila, 10).toString() == "MA"){
            f.cboUf.setSelectedIndex(0);
        } else if(tabela.getValueAt(fila, 10).toString() == "PI"){
            f.cboUf.setSelectedIndex(1);
        } else if(tabela.getValueAt(fila, 10).toString() == "PA"){
            f.cboUf.setSelectedIndex(2);
        } else if(tabela.getValueAt(fila, 10).toString() == "TO"){
            f.cboUf.setSelectedIndex(3);
        }
        //0 - inglês;\n1 - espanhol;\n2 - francês;\n3 - inglês e espanhol;\n
        //4 - inglês e francês;\n5 - espanhol e fracês.\n6 - inglês, espanhol e francês.
        if(tabela.getValueAt(fila, 11).toString() == "Inglês"){
            f.jcbIngles.setSelected(true);
        } else if (tabela.getValueAt(fila, 11).toString() == "Espanhol"){
            f.jcbEspanhol.setSelected(true);
        }else if (tabela.getValueAt(fila, 11).toString() == "Francês"){
            f.jcbFrances.setSelected(true);
        }else if (tabela.getValueAt(fila, 11).toString() == "Inglês e Espanhol"){
            f.jcbIngles.setSelected(true);
            f.jcbEspanhol.setSelected(true);
        }else if (tabela.getValueAt(fila, 11).toString() == "Inglês e Francês"){
            f.jcbIngles.setSelected(true);
            f.jcbFrances.setSelected(true);
        }else if (tabela.getValueAt(fila, 11).toString() == "Espanhol e Francês"){
            f.jcbEspanhol.setSelected(true);
            f.jcbFrances.setSelected(true);
        }else if (tabela.getValueAt(fila, 11).toString() == "Inglês, Espanhol e Francês"){
            f.jcbIngles.setSelected(true);
            f.jcbEspanhol.setSelected(true);
            f.jcbFrances.setSelected(true);
        }
    
        f.lblId.setText(tabela.getValueAt(fila, 0).toString());
        
        f.setVisible(true);
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        int fila = tabela.getSelectedRow();
        
        Sentencias s = new Sentencias();
        s.setId(Integer.parseInt(tabela.getValueAt(fila, 0).toString()));
        
        if(JOptionPane.showConfirmDialog(this, "Deseja Excluir Registro", "", 
                JOptionPane.OK_CANCEL_OPTION) == JOptionPane.OK_OPTION){
            if(Funcoes.isDelete(s)){
                Funcoes.setListar("");
                JOptionPane.showMessageDialog(this, "Usuário excluído com sucesso", "Informação", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao Excluir registro", "Erro", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

