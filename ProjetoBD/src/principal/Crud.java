    private void bntEliminarTodosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntEliminarTodosActionPerformed
        // TODO add your handling code here:
        if(JOptionPane.showConfirmDialog(this, "Deseja Excluir TODOS os Registros", "", 
                JOptionPane.OK_CANCEL_OPTION) == JOptionPane.OK_OPTION){
            if(Funcoes.isTruncate()){
                Funcoes.setListar("");
                JOptionPane.showMessageDialog(this, "TODOS os Usuários excluído com sucesso", "Informação", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao Excluir os registros", "Erro", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_bntEliminarTodosActionPerformed

